DATAS SEGMENT
ARRAYX DB 1,2,3
ODD DB 0H
EVN DB 0H
DATAS ENDS

CODE SEGMENT
MAIN PROC FAR
ASSUME CS:CODE,DS:DATAS
MOV AX,DATAS
MOV DS,AX
MOV CL,03H
LEA SI,ARRAYX
REPEAT:
MOV AH,00H
MOV AL,[SI]
MOV BL,02H
DIV BL
CMP AH,00H
JZ ISEVEN
INC ODD
JMP TERMINATE
ISEVEN:
INC EVN
TERMINATE:
INC SI
DEC CL
JNZ REPEAT
MOV BL,ODD
MOV BH,EVN
MOV AX,4C00H
INT 21H
NOP
CODE ENDS
END MAIN
	