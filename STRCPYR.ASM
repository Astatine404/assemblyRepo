.MODEL SMALL
.STACK 64
.DATA
	IPARR DB 6 DUP(30H)
	OPARR DB 6 DUP(30H)
.CODE
MAIN PROC FAR
MOV AX,@DATA
MOV DS,AX

#TAKING INPUT
MOV CL,06H
LEA SI,IPARR
REPEATIP:
MOV AH,10H
INT 16H
MOV [SI],AL
INC SI
DEC CL
JNZ REPEATIP

#STORING REVERSE IN OPARR
MOV CL,06H
LEA SI,IPARR
LEA DI,OPARR
ADD SI,05H
REPSTORE:
MOV BL,[SI]
MOV [DI],BL
INC DI
DEC SI
DEC CL
JNZ REPSTORE

#PRINTING OPARR
MOV CL,06H
LEA SI,OPARR
RDISPL:
MOV DL,[SI]
MOV AH,02H
INT 21H
INC SI
DEC CL
JNZ RDISPL
MOV AX,4C00H
INT 21H
NOP
MAIN ENDP
END MAIN