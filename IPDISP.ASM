.MODEL SMALL
.STACK 64
.DATA
	IPARR DW 5 DUP(0)
.CODE
MAIN PROC FAR
MOV AX,@DATA
MOV DS,AX
MOV CL,05H
LEA SI,IPARR
REPEAT:
MOV AH,10H
INT 16H
MOV [SI],AL
INC SI
DEC CL
JNZ REPEAT
MOV CL,05H
LEA SI,IPARR
RDISPL:
MOV DL,[SI]
MOV AH,02H
INT 21H
INC SI
DEC CL
JNZ RDISPL
MOV AX,4C00H
INT 21H
NOP
MAIN ENDP
END MAIN