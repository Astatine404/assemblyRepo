.MODEL SMALL
.STACK 64

.DATA
ARR DB 5 DUP(0)

.CODE
MAIN PROC FAR
MOV AX,@DATA
MOV DS,AX

#TAKING INPUT
MOV CL,05H
LEA SI,ARR
IPLOOP:
MOV AH,10H
INT 16H
MOV [SI],AL
INC SI
DEC CL
JNZ IPLOOP

#BUBBLE SORT
MOV CH,04H
OUTLOOP:
LEA SI,ARR
MOV CL,CH
INLOOP:
MOV BL,[SI]
MOV BH,[SI+01H]
CMP BH,BL
JBE EXCHG
JMP SKIP
EXCHG:
XCHG BH,BL
MOV [SI],BL
MOV [SI+01H],BH
SKIP:
INC SI
DEC CL
JNZ INLOOP
DEC CH
JNZ OUTLOOP

#PRINT ARRAY
LEA SI,ARR
MOV CL,05H
OPLOOP:
MOV DL,[SI]
MOV AH,02H
INT 21H
INC SI
DEC CL
JNZ OPLOOP

MOV AX,4C00H
INT 21H
NOP
MAIN ENDP
END MAIN