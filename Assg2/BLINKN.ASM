.MODEL SMALL
.STACK 64
.DATA
	MSG DB 'SAURABH','$'
	CNT DB ?

.CODE
MOV AX,@DATA
MOV DS,AX

MOV CNT,50D

INFLP:
#SET ACTIVE PAGE
MOV AH,05H
MOV AL,00H
INT 10H

#CLEAR SCREEN
MOV AH,06H
MOV BH,71H
MOV CX,0000H
MOV DX,184FH
INT 10H

#IDLE LOOPS
MOV BX,9FFFH
LP1:
MOV CL,00H
DEC BX
JNZ LP1

#SET CURSOR TO MIDDLE
MOV AH,02H
MOV BH,00D
MOV DH,12D
MOV DL,35D
INT 10H

#PRINT STRING
MOV AH,09H
LEA DX,MSG
INT 21H

#BEEP
MOV AH,02H
MOV DL,07H
INT 21H


#IDLE LOOPS
MOV BX,9FFFH
LP2:
MOV CL,00H
DEC BX
JNZ LP2

DEC CNT
JNZ INFLP

MOV AX,4C00H
INT 21H

END