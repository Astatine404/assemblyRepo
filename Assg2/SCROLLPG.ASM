.MODEL SMALL
.STACK 64
.DATA 
 MSG0 DB 'THIS IS PAGE 1','$'
 MSG1 DB 'THIS IS PAGE 2','$'
 MSG2 DB 'THIS IS PAGE 3','$'
 MSG3 DB 'THIS IS PAGE 4','$'
 CNT DB ?
 CURRPG DB ?
 INFCNT DB ?
.CODE
MOV AX,@DATA
MOV DS,AX


MOV INFCNT,03H
INFLP:
#SET ACTIVE PAGE
MOV AH,05H
MOV AL,00H
INT 10H

#CLEAR SCREEN
MOV AH,06H
MOV BH,71H
MOV CX,0000H
MOV DX,184FH
INT 10H

#SET CURSOR TO LAST LINE
MOV AH,02H
MOV BH,00D
MOV DH,24D
MOV DL,00D
INT 10H

#PRINT STRING
MOV AH,09H
LEA DX,MSG0
INT 21H

#SET ACTIVE PAGE
MOV AH,05H
MOV AL,01H
INT 10H

#SET CURSOR TO LAST LINE
MOV AH,02H
MOV BH,01D
MOV DH,24D
MOV DL,00D
INT 10H

#PRINT STRING
MOV AH,09H
LEA DX,MSG1
INT 21H

#SET ACTIVE PAGE
MOV AH,05H
MOV AL,02H
INT 10H

#SET CURSOR TO LAST LINE
MOV AH,02H
MOV BH,02D
MOV DH,24D
MOV DL,00D
INT 10H

#PRINT STRING
MOV AH,09H
LEA DX,MSG2
INT 21H

#SET ACTIVE PAGE
MOV AH,05H
MOV AL,03H
INT 10H

#SET CURSOR TO LAST LINE
MOV AH,02H
MOV BH,03D
MOV DH,24D
MOV DL,00D
INT 10H

#PRINT STRING
MOV AH,09H
LEA DX,MSG3
INT 21H

MOV CURRPG,00H
PAGELP:
#SET ACTIVE PAGE
MOV AH,05H
MOV AL,CURRPG
INT 10H

#SCROLL SCREEN
MOV CNT,24D
SCROLLP:
MOV AH,06H
MOV AL,01H
MOV BH,61H
MOV CX,0000H
MOV DX,184FH
INT 10H

#IDLE LOOPS
MOV BX,9FFFH
LP1:
DEC BX
JNZ LP1

DEC CNT
JNZ SCROLLP
INC CURRPG
CMP CURRPG,04H
JNZ PAGELP
DEC INFCNT
JZ EXIT
JMP INFLP
EXIT:

MOV AX,4C00H
INT 21H

END