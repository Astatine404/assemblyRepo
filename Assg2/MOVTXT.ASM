.MODEL SMALL
.STACK 64
.DATA
	DUM DW 8888
	MSG DB 'HELLO','$'
	CNT DB 00
	
.CODE
MOV AX,@DATA
MOV DS,AX

#LEFT TO RIGHT
LTOR:

#CLEAR SCREEN
MOV AH,06H
MOV BH,71H
MOV CX,0000H
MOV DX,184FH
INT 10H

#SET CURSOR
MOV AH,02H
MOV BH,00D  #PAGE NUMBER
MOV DH,12D
MOV DL,CNT
INT 10H

#PRINT STRING
MOV AH,09H
LEA DX,MSG
INT 21H


#IDLE LOOPS
MOV BX,9F9FH
LP1:
DEC BX
JNZ LP1

INC CNT
CMP CNT,75D
JNZ LTOR

#RIGHT TO LEFT
MOV CNT,75D
RTOL:

#CLEAR SCREEN
MOV AH,06H
MOV BH,71H
MOV CX,0000H
MOV DX,184FH
INT 10H

#SET CURSOR
MOV AH,02H
MOV BH,00D  #PAGE NUMBER
MOV DH,12D
MOV DL,CNT
INT 10H

#PRINT STRING
MOV AH,09H
LEA DX,MSG
INT 21H


#IDLE LOOPS
MOV BX,9F9FH
LP2:
DEC BX
JNZ LP2

DEC CNT
JNZ RTOL

#TOP TO BOTTOM
MOV CNT,00D
TTOB:

#CLEAR SCREEN
MOV AH,06H
MOV BH,71H
MOV CX,0000H
MOV DX,184FH
INT 10H

#SET CURSOR
MOV AH,02H
MOV BH,00D  #PAGE NUMBER
MOV DH,CNT
MOV DL,40D
INT 10H

#PRINT STRING
MOV AH,09H
LEA DX,MSG
INT 21H


#IDLE LOOPS
MOV BX,9F9FH
LP3:
DEC BX
JNZ LP3

INC CNT
CMP CNT,24D
JNZ TTOB

MOV AX,4C00H
INT 21H


END