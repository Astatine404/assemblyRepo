DATAS SEGMENT
N DB ?
ARRAYX DB 30 DUP(?)
SUM DB 0
AVG DW 0
DATAS ENDS

CODE SEGMENT
MAIN PROC FAR
ASSUME CS:CODE,DS:DATAS
MOV AX,DATAS
MOV DS,AX

#TAKING INPUT FOR N
MOV AH,10H
INT 16H
SUB AL,30H
MOV N,AL

#TAKING INPUT FOR ARRAYX
MOV CL,N
LEA SI,ARRAYX
IPLP:
MOV AH,10H
INT 16H
SUB AL,30H
MOV [SI],AL
INC SI
DEC CL
JNZ IPLP

#FIND SUM AND AVG
MOV CL,N
LEA SI,ARRAYX
MOV AX,0000H
REPEAT:
ADD AL,[SI]
INC SI
DEC CL
JNZ REPEAT
MOV SUM,AL
MOV CL,N
DIV CL
MOV AVG,AX

#PRINTING SUM
MOV CH,0AH
MOV AH,00H
MOV AL,SUM
DIV CH
MOV CL,AH
#PRINT QUOTIENT
MOV DL,AL
ADD DL,30H
MOV AH,06H
INT 21H
#PRINT REMAINDER
MOV DL,CL
ADD DL,30H
MOV AH,06H
INT 21H

#PRINT NEWLINE
MOV DL,0AH
MOV AH,02H
INT 21H

#PRINTING AVG
MOV BX,AVG
MOV DL,BL
ADD DL,30H
MOV AH,06H
INT 21H
#PRINT DOT
MOV DL,2EH
MOV AH,06H
INT 21H

MOV AL,BH
MOV AH,00H
MOV CH,0AH
MUL CH
MOV CL,N
DIV CL
#PRINT QUOTIENT
MOV DL,AL
ADD DL,30H
MOV AH,06H
INT 21H

MOV AX,4C00H
INT 21H
CODE ENDS
END MAIN