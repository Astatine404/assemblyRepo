.MODEL SMALL
.STACK 64
.DATA
	IPARR1 DB 6 DUP(30H)
	IPARR2 DB 6 DUP(30H)
.CODE
MAIN PROC FAR
MOV AX,@DATA
MOV DS,AX

#TAKING INPUT1
MOV CL,06H
LEA SI,IPARR1
REPEAT1:
MOV AH,10H
INT 16H
MOV [SI],AL
INC SI
DEC CL
JNZ REPEAT1

#TAKING INPUT2
MOV CL,06H
LEA SI,IPARR2
REPEAT2:
MOV AH,10H
INT 16H
MOV [SI],AL
INC SI
DEC CL
JNZ REPEAT2

#COMPARING STRINGS
MOV CL,06H
LEA SI,IPARR1
LEA DI,IPARR2
CHECK:
MOV BL,[SI]
MOV BH,[DI]
CMP BL,BH
JZ INCREMENT
JMP DISPLAYFALSE
INCREMENT:
INC SI
INC DI
DEC CL
JNZ CHECK
DISPLAYTRUE:
MOV DL,01H
ADD DL,30H
MOV AH,02H
INT 21H
JMP TERMINATE
DISPLAYFALSE:
MOV DL,00H
ADD DL,30H
MOV AH,02H
INT 21H

TERMINATE:
MOV AX,4C00H
INT 21H
NOP
MAIN ENDP
END MAIN