.MODEL SMALL
.STACK 64
.DATA
	N DB 00H
	INARR DB 30 DUP(30H)
.CODE
MAIN PROC FAR
MOV AX,@DATA
MOV DS,AX

#INPUT N
MOV AH,10H
INT 16H
SUB AL,30H
MOV N,AL

#INPUT THE INTEGER
LEA SI,INARR
MOV CL,N
IPLP:
MOV AH,10H
INT 16H
SUB AL,30H
MOV [SI],AL
INC SI
DEC CL
JNZ IPLP

#DISPLAY THE INTEGER
LEA SI,INARR
MOV CL,N
DISPLP:
MOV DL,[SI]
ADD DL,30H
MOV AH,06H
INT 21H
INC SI
DEC CL
JNZ DISPLP

MOV AX,4C00H
INT 21H

MAIN ENDP
END MAIN