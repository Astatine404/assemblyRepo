.MODEL SMALL
.STACK 64

.DATA
STR1 DB 'HELLO:EF;a',0
STR2 DB 10 DUP(30H)

.CODE
MAIN PROC FAR
MOV AX,@DATA
MOV DS,AX

#UPPER TO LOWER
MOV CL,0AH
LEA SI,STR1
MODLOOP:
MOV BL,[SI]
CMP BL,41H
JAE CHK
JMP SKIP
CHK:
CMP BL,5AH
JBE UPPERCASE
JMP SKIP
UPPERCASE:
XOR BL,20H
MOV [SI],BL
SKIP:
INC SI
DEC CL
JNZ MODLOOP

#REMOVE ANY OTHER CHAR
MOV CL,0AH
LEA SI,STR1
LEA DI,STR2
CHLOOP:
MOV BL,[SI]
CMP BL,61H
JAE CHK2
JMP SKPCPY
CHK2:
CMP BL,7AH
JBE CPYPT
JMP SKPCPY
CPYPT:
MOV [DI],BL
INC DI
SKPCPY:
INC SI
DEC CL
JNZ CHLOOP
MOV BYTE PTR [DI],00H

#PRINT
LEA DI,STR2
PNTLP:
MOV DL,[DI]
CMP DL,0H
JZ LVLOOP
MOV AH,02H
INT 21H
INC DI
JMP PNTLP
LVLOOP:
MOV AX,4C00H
INT 21H
NOP
MAIN ENDP
END MAIN