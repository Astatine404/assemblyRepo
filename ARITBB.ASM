.MODEL SMALL
.STACK 64
.DATA
	A DB 0AH
	B DB 05H
.CODE
MAIN PROC FAR
MOV AX,@DATA
MOV DS,AX

#ADDING
MOV AL,A
ADD AL,B
MOV AH,00H

#PRINTING ADDITION
MOV BL,0AH
DIV BL
 #PRINT QUOTIENT IN AL
MOV BH,AH
ADD AL,30H
MOV DL,AL
MOV AH,02H
INT 21H
 #PRINT REMAINDER IN BH
MOV DL,BH
ADD DL,30H
MOV AH,02H
INT 21H

#PRINT NEWLINE
MOV DL,0AH
MOV AH,02H
INT 21H

#SUBTRACTING
MOV AL,A
SUB AL,B
MOV AH,00H

#PRINTING SUBTRACTION
MOV BL,0AH
DIV BL
 #PRINT QUOTIENT IN AL
MOV BH,AH
ADD AL,30H
MOV DL,AL
MOV AH,02H
INT 21H
 #PRINT REMAINDER IN BH
MOV DL,BH
ADD DL,30H
MOV AH,02H
INT 21H

#PRINT NEWLINE
MOV DL,0AH
MOV AH,02H
INT 21H

#MULTIPLY
MOV AL,A
MOV BL,B
MUL BL

#PRINTING MULTIPLICATION
MOV BL,0AH
DIV BL
 #PRINT QUOTIENT IN AL
MOV BH,AH
ADD AL,30H
MOV DL,AL
MOV AH,02H
INT 21H
 #PRINT REMAINDER IN BH
MOV DL,BH
ADD DL,30H
MOV AH,02H
INT 21H

#PRINT NEWLINE
MOV DL,0AH
MOV AH,02H
INT 21H

#DIVIDE
MOV AL,A
MOV BL,B
MOV AH,00H
DIV BL

#PRINTING DIVISION
MOV BL,0AH
DIV BL
 #PRINT QUOTIENT IN AL
MOV BH,AH
ADD AL,30H
MOV DL,AL
MOV AH,02H
INT 21H
 #PRINT REMAINDER IN BH
MOV DL,BH
ADD DL,30H
MOV AH,02H
INT 21H

MOV AX,4C00H
INT 21H
NOP
MAIN ENDP
END MAIN
