.MODEL SMALL
.STACK 64
.DATA
	A DW 0AAAH
	B DW 0BBBH
.CODE
MAIN PROC FAR
MOV AX,@DATA
MOV DS,AX

#ADDING
MOV AX,A
ADD AX,B
MOV AH,00H

#PRINTING
MOV BL,0AH
DIV BL
 #PRINT QUOTIENT IN AL
MOV BH,AH
ADD AL,30H
MOV DL,AL
MOV AH,02H
INT 21H
 #PRINT REMAINDER IN BH
MOV DL,BH
ADD DL,30H
MOV AH,02H
INT 21H

MOV AX,4C00H
INT 21H
NOP
MAIN ENDP
END MAIN
